package com.route4me.sdk.model;

/**
 *
 * @author juan
 */
public class Links {

    private String optimization_problem;
    private String route;
    private String view;

    /**
     * @return the optimization_problem
     */
    public String getOptimization_problem() {
        return optimization_problem;
    }

    /**
     * @param optimization_problem the optimization_problem to set
     */
    public void setOptimization_problem(String optimization_problem) {
        this.optimization_problem = optimization_problem;
    }

    /**
     * @return the route
     */
    public String getRoute() {
        return route;
    }

    /**
     * @param route the route to set
     */
    public void setRoute(String route) {
        this.route = route;
    }

    /**
     * @return the view
     */
    public String getView() {
        return view;
    }

    /**
     * @param view the view to set
     */
    public void setView(String view) {
        this.view = view;
    }

}
